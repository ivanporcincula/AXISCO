export function displayMK(){

    let productHTML='';
    let product_id = '';
    let name = '';
    let img_src = '';
    let description = '';
    let price = 0;

    let keyboard_header = true;
    let mouse_header=true;
    
    document.getElementById('page-product').innerHTML = "";
    let parent_element = document.getElementById('page-product');
    parent_element.insertAdjacentHTML('afterbegin', '<div class="product-row" id="keyboard"></div>');
    parent_element.insertAdjacentHTML('beforeend', '<div class="product-row" id="mouse"></div>');
    
    let keyboard_element = document.getElementById('keyboard');
    let mouse_element = document.getElementById('mouse');  
    
    let keyboardHTML='';
    let mouseHTML='';

        for(let i = 0 ; i < keyboard_and_mouse_products.length ; i++){


            product_id = keyboard_and_mouse_products[i].product_id;
            name = keyboard_and_mouse_products[i].name;
            img_src = keyboard_and_mouse_products[i].img_src;
            price = keyboard_and_mouse_products[i].price;


            if(keyboard_and_mouse_products[i].category == "Keyboard"){
                
                if(keyboard_header){
                    
                    
                    keyboard_element.insertAdjacentHTML('beforebegin', '<h1>Keyboard</h1>');
                    keyboard_header=false;
                    
                }
                description =  
                keyboard_and_mouse_products[i].specifications.color + ', ' + 
                keyboard_and_mouse_products[i].specifications.switch_color + ', ' + 
                keyboard_and_mouse_products[i].specifications.hot_swap + ', ' +
                keyboard_and_mouse_products[i].specifications.connection;  
                keyboardHTML = productHTML;
               
            }

            else{

                
                if(mouse_header){
                    productHTML= '';
                    mouse_element.insertAdjacentHTML('beforebegin', '<h1>Mouse</h1>');
                    mouse_header=false;
                }
                description =  
                keyboard_and_mouse_products[i].specifications.color + ', ' + 
                keyboard_and_mouse_products[i].specifications.connection + ', ' + 
                keyboard_and_mouse_products[i].specifications.dpi;

                mouseHTML = productHTML;

            }

            productHTML += '<div class="product-column">\
                <div class="product-card">\
                                        \
                    <div class="overlay">\
                        <button class="cart-button" id="'+product_id+'">\
                            <i class="fas fa-shopping-cart" id="'+product_id+'"></i>\
                            <h6 id="'+product_id+'">ADD TO CART</h6>\
                        </button>\
                                \
                    </div>      \
                                \
                    <div class="product-id">\
                        <p>'+product_id+'</p>\
                    </div>\
                    \
                    <div class="product-img">\
                        <img class="product-image" id="image" src="'+img_src+'">\
                    </div>\
                    \
                    <div class="product-name">\
                        <hr>\
                        <h3>'+name+'</h3>\
                    </div>\
                    \
                    <div class="product-description">\
                        <p>'+description+'</p>\
                    </div>\
                    \
                    <div class="product-price">\
                        <h3>\
                        â‚±'+price.toLocaleString()+'.00\
                        </h3>\
                    </div>\
                </div>\
            </div>';
            
            
        }

        document.querySelector("#keyboard").innerHTML = keyboardHTML;
        document.querySelector("#mouse").innerHTML = mouseHTML;
    
}